#!/usr/bin/env python3
"""
This module provides a simple function to add two numbers.
"""

def add(a: float, b: float) -> float:
    """
    Adds two numbers and returns the result.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b

